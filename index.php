                        <h2>DESA SUNGAI BAUNG</h2>
                        <p style="padding-bottom: 10px; text-align: justify;">
                            Desa Sungai Baung adalah nama suatu wilayah di Kecamatan Rengat Barat Kabupaten Indragiri Hulu ini yang menurut beberapa tokoh masyarakat setempat dikenal karena sungai yang ada di desa tersebut memiliki keanehan, yaitu walaupun kemarau panjang, sungai tersebut tidak pernah kering dan konon banyak ikan Baung. Maka beberapa orang tokoh masyarakat memberikan nama desa tersebut dengan nama Desa Sungai Baung.
                        </p>
                        <p style="padding-bottom: 10px; text-align: justify;">Sebelumnya Desa Sungai Baung masih menjadi bagian Desa Bukit Petaling, dan pada tahun 2004 Desa Sungai baung memisahkan diri dengan Desa Bukit Petaling. Dan kepala desa pertama yang menjabat yaitu Bapak SALMUNZIRI sampai sekarang ini.</p>
                        <p style="padding-bottom: 10px; text-align: justify;">Dimasa pemerintahan Bapak Salmunziri, kegiatan pemerintahan desa lebih difokuskan untuk menata kelompok masyarakat yang sekarang menjadi dusun. Tata pemerintahan pun disusun dengan baik untuk memajukan Desa Sungai Baung serta berrtujuan untuk kesejahteraan masyarakat.</p>
                        <p style="padding-bottom: 10px; text-align: justify;">Pada tahun 2012 diadakan pemilihan kepala desa untuk yang kedua kalinya dan terpilihnya Bapak TRIMO yang menjabat hingga tahun 2018.
                            Di tahun 2018 menjelang pemilihan kepala desa serempak di tahun 2019, kepala Desa Sungai Baung dijabat oleh Penjabat sementara dari Kecamatan Rengat Barat yaitu Bapak HERIANTONI, SE.
                        </p>
                        <p style="padding-bottom: 10px; text-align: justify;">Pemilihan Kepala Desa Serempak di bulan 04 Desember 2019 Kepala Desa terpilih yaitu Bapak MUZAKIR, yang dilantik pada tanggal 17 Februari 2020. Dan akan memimpin pemerintahan Desa Sungai Baung dari tahun 2020 sampai dengan tahun 2026.</p>
